package ar.edu.unq.po2.tpTestYTestDoubles;

public class Carta {
	
	private int numero;
	private String palo;
	
	public Carta(int numero, String palo) {
		super();
		this.numero = numero;
		this.palo = palo;
	}

	public int getNumero() {
		return numero;
	}

	public String getPalo() {
		return palo;
	}

}
